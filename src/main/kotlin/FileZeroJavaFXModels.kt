fun createCubeMesh(size: Double, color: Color, inverted: Boolean = false): Mesh {
    val hs = size / 2.0
    val vertices = listOf(
        Vector3d(-hs, -hs, hs), Vector3d(hs, -hs, hs),
        Vector3d(hs, hs, hs), Vector3d(-hs, hs, hs),
        Vector3d(-hs, -hs, -hs), Vector3d(hs, -hs, -hs),
        Vector3d(hs, hs, -hs), Vector3d(-hs, hs, -hs)
    )
    var faces = listOf(
        listOf(0, 1, 2, 3),
        listOf(4, 7, 6, 5),
        listOf(3, 2, 6, 7),
        listOf(0, 4, 5, 1),
        listOf(1, 5, 6, 2),
        listOf(4, 0, 3, 7)

